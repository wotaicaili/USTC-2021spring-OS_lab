#include "io.h"

void init8259A(void) {
    // shield all the interrupt sources
    outb(0x21, 0XFF);
    outb(0xA1, 0xFF);

    // init master chip 0x20 ~ 0x21
    outb(0x20, 0x11);
    outb(0x21, 0x20);
    outb(0x21, 0x04);
    outb(0x21, 0x3);

    // init slave chip 0xA0 ~ 0xA1
    outb(0xA0, 0x11);
    outb(0xA1, 0x28);
    outb(0xA1, 0x02);
    outb(0xA1, 0x01);
}
